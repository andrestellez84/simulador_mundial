from typing import List, Tuple
from ..models.team import Team
from .groups import GROUPS

def generate_group_matchups() -> List[Tuple[str, Team, Team]]:
    """
    Retorna la lista de enfrentamientos (72 en total).
    Para cada uno de los 12 grupos formados por 4 equipos (T1, T2, T3, T4),
    el round robin es:
    T1 vs T2, T3 vs T4
    T1 vs T3, T2 vs T4
    T1 vs T4, T2 vs T3
    """
    matchups = []
    for group_name, teams in GROUPS.items():
        if len(teams) != 4:
            continue
        t1, t2, t3, t4 = teams
        # Matchday 1
        matchups.append((group_name, t1, t2))
        matchups.append((group_name, t3, t4))
        # Matchday 2
        matchups.append((group_name, t1, t3))
        matchups.append((group_name, t4, t2)) # Alternamos H/A
        # Matchday 3
        matchups.append((group_name, t4, t1))
        matchups.append((group_name, t2, t3))
        
    return matchups

GROUP_MATCHES = generate_group_matchups()

